Lanzado oficialmente el 11 de mayo de 2011 en más de 100 países, el Decenio de Acción para la Seguridad Vial tiene el objetivo oficial de estabilizar y luego reducir las fatalidades de tránsito en el mundo en 2020.
<br>
<strong>ACERCA DEL OBSERVATORIO DEL DECENIO</strong>
<br>
Un equipo de investigadores y académicos interesados en observar el progreso de México en la meta de salvar miles de vidas, a través de la implementación de la Estrategia Nacional de Seguridad Vial 2011-2020.
Nuestra Misión es, mostrar los avances de México en el Decenio de Acción y, al mismo tiempo, poner a disposición de autoridades, de tomadores de decisiones, de investigadores y de la población en general; datos, información y conocimiento pertinentes, que orienten y faciliten el desarrollo de políticas públicas, programas e intervenciones para el mejoramiento de la seguridad vial en todo el país.
